namespace Contender\Elements\Traits;

use Symfony\Component\CssSelector\CssSelectorConverter;

/**
 * Trait CssSelector2XPathTrait
 *
 * @category   Contender
 * @package    Contender\Elements\Traits
 * @subpackage Contender\Elements\Traits
 * @author     suzunone<suzunone.eleven@gmail.com>
 * @copyright  Project Contender
 * @license    MIT
 * @version    1.0
 * @link       https://github.com/suzunone/Contender
 * @see        https://github.com/suzunone/Contender
 * @since      2020/03/15
 */
trait CssSelector2XPathTrait
{
    /**
     * @param string $css_selector
     * @return string
     */
    protected function cssSelector2XPath(string $css_selector): string
    {
        $converter = new CssSelectorConverter();

        return $converter->toXPath($css_selector);
    }
}
